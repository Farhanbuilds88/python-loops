#For loop
for i in range(1, 8):
    print(i)

for j in range(10,20):
    print(j)

for a in range(1,15,2):
    print(a)

for j in range(1,100,5):
    print(j)

for num in range(1, 11):
    print("Table of", num)
    for i in range(1, 11):
        print(num, "x", i, "=", num * i)
    print()

for i in range(1, 6):
    print("*" * i)

for i in range(1, 8):
    if i > 3:
        print(i)

